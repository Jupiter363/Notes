"""
================================================================================
阶段：解析（Parse）—— 工作流第一步
================================================================================
本 Prompt 属于"需求解析"节点。在旅行规划 Agent 的工作流中，用户输入的
自然语言（例如"我想去成都玩三天，喜欢吃辣"）首先进入此 Prompt，由 LLM
将其抽取为结构化的旅行请求对象（TravelRequest）。

旅行请求对象包含：目的地、天数、预算、偏好类型、节奏等字段，下游的
规划节点（Plan）将严格依赖此结构化数据执行工具调用和计划生成。

若解析出错或缺字段，会导致后续规划和反思全部跑偏，因此本步骤对
Agent 可靠性至关重要。

================================================================================
概念解释：ChatPromptTemplate
================================================================================
ChatPromptTemplate 是 LangChain 提供的"对话式 Prompt 模板"类。
与普通字符串拼接不同，它理解 LLM 的消息模型（Message Model）——即
一次对话由多条消息组成，每条消息有"角色"（role）和"内容"（content）。

ChatPromptTemplate 的核心能力：
  1. `from_messages()` 类方法：从一个消息列表构建模板，列表中每个元素
     都是一个 (role, content) 元组。
  2. 占位符替换：content 中的 {变量名} 会在运行时被 format() 或管道
     invoke() 替换为实际值，生成最终的提示词文本。
  3. 多角色支持：同时承载 system、human、AI 等不同角色的消息。

================================================================================
概念解释：system 角色 vs human 角色
================================================================================
聊天模型（如 GPT-4、Claude）的消息分为多种角色：

  system（系统消息）
    - 位置：对话的最开头（第一条消息）
    - 作用：设定 AI 的行为边界、身份、规则、输出格式
    - 特点：对模型行为的影响力最大，用于"定义 AI 是什么"
    - 类比：给演员的"角色设定说明书"
    - 本文件中的系统消息定义了"你是一个旅行需求解析器"，以及解析规则

  human（用户消息）
    - 位置：system 之后，是模型的"输入"
    - 作用：携带当前对话中需要处理的具体数据
    - 特点：可以包含用户原始输入、上游节点的输出、工具返回的上下文
    - 类比：用户对演员说的"这句台词你来演一下"
    - 本文件中的用户消息携带 {user_input}，即用户原始自然语言输入

在 `from_messages` 列表中，通常第一个是 system（设规则），随后是 human
（给输入）。LLM 看到这两条消息后，按 system 设定的角色去处理 human 的输入。

================================================================================
概念解释：{format_instructions} 占位符与 PydanticOutputParser
================================================================================
{format_instructions} 是一个特殊的占位符，由 LangChain 的 Output Parser
在运行时自动注入。

工作机制：
  1. 我们在代码中创建 PydanticOutputParser(pydantic_object=TravelRequest)
  2. 调用 parser.get_format_instructions() 会得到一个字符串，内容是
     该 Pydantic 模型要求的 JSON Schema 说明
  3. ChatPromptTemplate 在 invoke 时，用这个字符串替换 {format_instructions}

为什么需要这个：
  - LLM 默认输出自由文本，但 Agent 的下游节点需要结构化的 Python 对象
  - PydanticOutputParser 给 LLM 施加约束："你必须输出这样的 JSON"
  - 配合 Pydantic 模型（TravelRequest），LLM 输出会被自动反序列化为
    类型安全的 Python 对象，字段有类型校验和默认值
  - 这对应 Stage 2 笔记 §3 Structured Output：
    结构化输出是让 LLM 从"写作文"变成"填表格"——可靠、可解析、可验证

================================================================================
约束说明：为什么写"不要编造"（No Hallucination）
================================================================================
"如果某个字段没有明确出现，请返回 null 或空列表，不要编造"
——这是 Agent 可靠性的关键约束，原因如下：

  1. 防止幻觉链式传播
     LLM 在缺少信息时有强烈的"补全"倾向。如果用户没说预算，
     LLM 可能擅自编造一个"5000元"，后续规划就按错误的预算执行。
     一条幻觉 → 下游全部偏离 → 最终计划不可用。

  2. null vs 编造：语义完全不同
     null 表示"未知，需要后续节点处理"（如设置默认值、追问用户）
     编造的值表示"用户提供了"（实际上没有），导致静默错误

  3. 对应 Stage 2 笔记 §5 Reflection：
     反思/审查节点（reflection_prompt）会检查计划是否匹配需求。
     如果需求本身是编造的，审查就失去了"锚点"——你在审查一个
     根据幻觉需求做的计划，怎么查都是错的。

  4. 这是所有 Agent 系统的通用铁律：
     不要替用户做决定，不要在信息不完整时假设数据。
     Agent 的核心能力是"何时调用工具获取信息"以及"何时承认不知道"。

================================================================================
技术细节：偏好列表的设计
================================================================================
偏好类型用一个固定枚举集合约束（美食、轻松、自然风光……），而不是
让 LLM 自由发挥。原因：
  - 枚举约束 = 下游节点可以用 if/else 精确匹配偏好做决策
  - 自由文本 = 下游无法可靠判断"这个偏好到底是什么意思"
  - 这是结构化输出的又一体现：将自然语言的模糊性在入口处就用枚举消除
"""

from langchain_core.prompts import ChatPromptTemplate

parse_prompt = ChatPromptTemplate.from_messages([
    ("system", """你是一个旅行需求解析器。请从用户输入中抽取结构化旅行需求。
如果某个字段没有明确出现，请返回 null 或空列表，不要编造。

偏好类型包括：美食、轻松、自然风光、历史文化、购物、亲子、拍照
节奏类型包括：轻松、适中、紧凑

{format_instructions}"""),
    ("human", "用户输入：{user_input}")
])
