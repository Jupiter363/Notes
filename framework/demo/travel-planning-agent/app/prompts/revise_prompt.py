"""
================================================================================
阶段：修正（Revise）—— 工作流第四步（条件触发）
================================================================================
本 Prompt 属于"计划修正"节点，仅在 Reflection 节点判定 need_revision=true
时触发。修正完成后，输出返回 Plan 节点（或直接进入下一次 Reflection），
形成 LangGraph 中的条件循环边：

  Plan → Reflection → [need_revision?]
            ↓ 是              ↓ 否
          Revise            END（输出 TravelPlan）
            ↓
         Plan（重新生成）或 Reflection（重新审查）

Revise 不是"微调"，而是"根据 Reflection 的具体 issues 做针对性修改"。
它的输入是 Reflection 的完整审查结果，输出是修正后的 TravelPlan。

================================================================================
概念解释：ChatPromptTemplate 和 from_messages
================================================================================
ChatPromptTemplate.from_messages() 接收 (角色, 内容) 元组列表，
构建一个对话格式的 Prompt 模板。

在本文件中：
  - 第一个元组 ("system", ...) 定义修订规则和约束
  - 第二个元组 ("human", ...) 携带需要修订的上下文数据

LangChain 在 invoke() 时：
  1. 扫描内容中的 {变量名} 模式
  2. 用 kwargs 中对应参数的值替换
  3. 将元组转换为 LangChain BaseMessage 子类（SystemMessage / HumanMessage）
  4. 组装为消息列表传给 LLM

最终 LLM 收到的是一个完整的、角色分明、变量已填充的对话。

================================================================================
system vs human 角色在 Revise 中的具体含义
================================================================================
system（系统消息）—— 8 条修正规则：
  定义了 Revise 节点的"操作手册"。LLM 读到这些规则时，会将其理解为
  必须遵守的硬约束。关键规则解析见下方。

human（用户消息）—— 4 个数据占位符：
  {request}    —— 用户的原始结构化需求（不可动摇的锚点，对应规则 4）
  {context}    —— 工具查询的原始数据（修正时必须基于真实数据）
  {draft_plan} —— 待修正的当前计划（即为 Plan 节点的输出）
  {reflection} —— Reflection 节点的审查结果（包含 issues 和 suggestions）

  注意：这里同时传入了 draft_plan 和 reflection。
  - draft_plan 是"需要改什么"
  - reflection 是"为什么需要改 / 改的方向"
  两者配合，LLM 才能做"有目标的修改"而非"盲目重写"。

================================================================================
8 条修正规则的设计意图（逐条解析）
================================================================================

规则 1: "保持 TravelPlan 结构完整"
  Revise 的输出必须是完整且符合 Pydantic 模型定义的 TravelPlan 对象。
  不能只返回"修改的部分"——下游节点（Reflection 的下一轮审查）需要
  完整的计划来重新评分。

规则 2: "优先解决 blocking_issues 中的阻断性问题"
  blocking_issues 是 Reflection 标注的"致命问题"，如预算严重超支、
  目的地错误、天数不匹配。这些问题不解决，其他小修改没有意义。
  "优先级"概念是 Agent 工作流设计的关键——让 LLM 先解决最重要的问题。

规则 3: "针对 suggestions 中的每条建议逐一修改或说明"
  要求 LLM 对每条建议都有响应（修改 or 说明不修改的理由），防止
  LLM 选择性忽略某些建议。这是 Reflection→Revise 循环收敛的关键：
  如果每次只改一半建议，循环永远不会结束。

规则 4: "不要改变用户核心需求（目的地、天数、偏好）"
  这是"锚点"保护规则。Revise 的权限是"修改执行方案"，不是"篡改需求"。
  对应 Stage 2 笔记 §5 Reflection：修正的方向是让计划更好地服务需求，
  而非改变需求来适应计划。

规则 5: "若预算超支，应替换高价活动为低价替代方案"
  提供了具体的修改策略（替换而非删除），让 LLM 在修正时有可操作性
  的指导，而非空泛的"降低预算"。

规则 6: "若行程过紧，应减少活动数量（每天 3-4 个为宜）或增加休息时段"
  同规则 5，将 plan_prompt 中"每天 2-3 个活动"的规则在这里重申为
  "修正时的目标区间"，保持两阶段标准一致。

规则 7: "若天气有风险，应将户外活动优先安排在天气较好的日期或时段"
  这是一个"重新排序"策略（reorder），而非简单删除户外活动。
  体现了 Revise 的灵活性——不是一刀切，而是智能调整。

规则 8: "修正后的计划应比修正前有明显改善"
  这个约束防止 LLM 做"原地踏步"式的修改（改几个字就返回）。
  Reflection 节点在下一轮审查时会检查这一点——如果评分没有提升，
  需要继续修正。这是循环收敛的驱动力。

================================================================================
{format_instructions} 与 PydanticOutputParser
================================================================================
{format_instructions} 在 Revise 中的特殊之处：
  - Revise 输出的仍然是 TravelPlan（与 Plan 节点输出相同结构）
  - 这意味着 PydanticOutputParser 绑定的是同一个 Pydantic 模型
  - Plan 和 Revise 共享输出 Schema，保证了"Plan → Revise → Plan"循环中
    数据结构的一致性——LangGraph 的状态 key 不需要改变

这对应 Stage 2 笔记 §3 Structured Output 的原则：
  多个节点的输出如果是同一概念（如"计划"），应使用同一个 Pydantic
  模型定义，避免类型不一致导致的解析失败。

================================================================================
约束说明："不要编造"在 Revise 中的体现
================================================================================
Revise 的"不编造"体现在：
  1. 规则 4 明确禁止改变核心需求 → 不能为了让计划"看起来更好"而篡改目的地
  2. 规则 5/6/7 给出了具体的替代策略 → 用"真实存在的替代方案"而不是编造
  3. 规则 3 要求逐条响应 suggestions → 每个修改都有 Traceability（可追溯性）

Agent 可靠性的核心：
  Revise 是"有方向的修改"，而非"重新想象"。
  每次修改都对应 Reflection 的一条具体反馈，形成闭环。
  如果让 LLM 自由发挥 Revise，它会倾向于"全部重写"，引入新的不确定性。

================================================================================
与 Reflection Prompt 的协作关系
================================================================================
Revise 的输入 {reflection} 正是 Reflection 的输出 ReflectionResult。
两者形成闭环：

  Reflection 的 issues[i]  →  Revise 规则 2/3 解决它
  Reflection 的 suggestions[i]  →  Revise 规则 3 逐条响应
  Reflection 的 blocking_issues  →  Revise 规则 2 优先处理

这种"审查→修正→再审查"的循环模式是 Agent 工作流的核心设计模式，
对应 Stage 2 笔记 §5 Reflection 中描述的 Self-Correction Loop。
"""

from langchain_core.prompts import ChatPromptTemplate

revise_prompt = ChatPromptTemplate.from_messages([
    ("system", """你是一个旅行计划修正器。请根据 Reflection 的 issues 和 suggestions 修正当前计划。

要求：
1. 保持 TravelPlan 结构完整；
2. 优先解决 blocking_issues 中的阻断性问题；
3. 针对 suggestions 中的每条建议逐一修改或说明；
4. 不要改变用户核心需求（目的地、天数、偏好）；
5. 若预算超支，应替换高价活动为低价替代方案，或在 risk_notes 中说明不可避免的原因；
6. 若行程过紧，应减少活动数量（每天 3-4 个为宜）或增加休息时段；
7. 若天气有风险，应将户外活动优先安排在天气较好的日期或时段；
8. 修正后的计划应比修正前有明显改善。

{format_instructions}"""),
    ("human", """用户需求：
{request}

工具上下文：
{context}

当前计划：
{draft_plan}

反思结果：
{reflection}""")
])
