"""
交通查询工具 (Transport Query Tool)
====================================

在旅行规划 Agent 的架构中，本工具负责**提供目的地的市内交通信息和出行建议**。

Agent 调用链中的位置：
  用户："我从成都双流机场怎么去宽窄巷子？"
       ↓
  LLM → 调用 search_transport(city="成都")
       ↓
  返回地铁/公交/出租车/共享单车/机场交通 + 贴士
       ↓
  LLM → "地铁可直达双流机场，到宽窄巷子坐2号线在通惠门站下"

数据特点：
  - 半结构化：metro/bus/taxi/bike 是分类键，tips 是自由文本列表
  - 静态数据为主：交通方式变化慢，mock 数据也有较高参考价值
  - 与景点工具联动：LLM 结合景点位置和交通信息规划路线

相关概念（§3 Tool Calling）：
  - 工具的"领域知识"封装：交通信息被封装在本工具内，Agent 不需要知道
    成都有几条地铁线——它只需要调用工具来获取
"""

from datetime import date
from langchain_core.tools import tool


@tool
def search_transport(city: str, start_date: str = "") -> dict:
    """
    查询市内交通建议和出行提示。

    LLM 调用此工具来获取目的地的公共交通信息。
    start_date 参数目前未在 mock 数据中使用（预留用于后续接入实时交通 API，
    例如查询特定日期的地铁运营时间或道路封闭通知）。

    Args:
        city:       城市名称
        start_date: 出发日期（可选），用于未来接入实时交通信息

    Returns:
        dict: 符合 ToolResult 协议的交通信息
              - data: dict，包含 metro/bus/taxi/bike/airport/tips 六个分类键
    """  # noqa: D401
    # -----------------------------------------------------------------
    # 交通信息数据库
    # -----------------------------------------------------------------
    # 结构设计：dict of dict
    #   外层 key = 城市名
    #   内层 dict = 按交通方式分类的文本信息 + 出行贴士列表
    #
    # 为什么用 dict 的固定 key 而不是 list？
    #   交通方式种类是固定的（地铁、公交、出租、共享单车、机场），
    #   用固定 key 的 dict 比 list 更容易被 LLM 按类别引用。
    #   例如 LLM 可以说"关于地铁：{metro}，关于出租：{taxi}"。
    transport_info = {
        "成都": {
            "metro": "地铁线路覆盖主城区，1/2/3/4/7号线可到达大多数景点",
            "bus": "公交线路密集，天府通卡通用",
            "taxi": "起步价8元，网约车方便",
            "bike": "共享单车覆盖全城，短距离推荐",
            "airport": "双流机场和天府机场，地铁可直达双流",
            # tips 是 list[str] 而非 str，因为贴士通常有多条
            # 用 list 方便 LLM 逐条引用或展示为项目符号列表
            "tips": [
                "高峰期（7:30-9:00, 17:30-19:00）地铁较拥挤",
                "去都江堰可在犀浦站坐城际列车",
            ],
        },
        # 默认兜底：未知城市的通用交通建议
        "default": {
            "metro": f"{city}地铁覆盖主城区",
            "bus": "公交线路可达主要景点",
            "taxi": "网约车方便快捷",
            "bike": "共享单车适合短途出行",
            "airport": f"{city}机场有公共交通连接市区",
            "tips": ["建议下载当地交通App查询实时信息"],
        },
    }

    # -----------------------------------------------------------------
    # 获取数据 + 兜底
    # -----------------------------------------------------------------
    # transport_info.get(city, transport_info["default"])
    # 与 attraction_tool / food_tool 相同的兜底模式
    return {
        "data": transport_info.get(city, transport_info["default"]),
        "source": "mock_transport",
        "updated_at": str(date.today()),
        "confidence": "mock",
        "error": None,
    }


# =============================================================================
# 交通工具的特殊性
# =============================================================================
# 与其他工具（天气、景点、美食）相比，交通工具有两个独特之处：
#
# 1. **数据"保质期"长**：
#    天气每天变、美食可能换菜单，但地铁线路、出租车起步价通常以年为单位变化。
#    因此交通 mock 数据在开发阶段的可信度相对更高。
#
# 2. **与景点工具的隐性联动**：
#    LLM 在收到景点列表后，可以查询交通信息来判断"去都江堰怎么走"。
#    这种"工具间的组合推理"是 Agent 的核心能力之一 —— 单个工具不知道
#    另一个工具的存在，但 LLM 知道如何编排它们。
#
# 3. **start_date 的预留设计**：
#    当前未使用，但函数签名中保留了这个参数。这是"面向未来"的设计：
#    后续接入实时 API 时，可以通过日期查询特定日的地铁时刻表或封路通知，
#    而不需要修改函数签名（避免破坏所有调用方）。
# =============================================================================
